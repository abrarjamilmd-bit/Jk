// Simple utility function to safely grab translations and interpolate placeholders
export function t(lang: Language, key: keyof typeof translations["bn"], interpolations?: Record<string, string | number>): string {
  const dictionary = translations[lang] || translations["bn"];
  let text = dictionary[key] || translations["bn"][key] || String(key);
  
  if (interpolations) {
    Object.entries(interpolations).forEach(([k, v]) => {
      text = text.replace(new RegExp(`{${k}}`, "g"), String(v));
    });
  }
  return text;
}
